"""
Day 2 - Data Types in Python
Demonstrates the different data types available in Python.
This module shows:
- Integer (int) - whole numbers
- Float - decimal numbers
- String (str) - text
- Character - single character in string form
- Boolean (bool) - True or False values
"""

# Integer data type - stores whole numbers
x = 100
# Float data type - stores decimal numbers
y = 10.5
# String data type - stores text/sequence of characters
s = "Welcome"
# Character stored as a string (Python doesn't have a separate char type)
c = 'A'
# Boolean data type - stores True or False values
b = True  # or b = False

# Print the type of each variable
print(type(x))  # Output: <class 'int'>
print(type(y))  # Output: <class 'float'>
print(type(s))  # Output: <class 'str'>
print(type(c))  # Output: <class 'str'>
print(type(b))  # Output: <class 'bool'>
