
# Assigning values
name = "Amar"        # string
age = 30             # integer
height = 5.9         # float
is_developer = True  # boolean

# Printing variables
print(name)
print(age)
print(height)
print(is_developer)

# Reassigning
age = age + 1
print("Next year age:", age)

# Multiple assignment
x, y, z = 10, 20, 30
print(x, y, z)


# Variable Types in Action

# String concatenation
greeting = "Hello"
target = "World"
print(greeting + " " + target)   # Output: Hello World

# Arithmetic
a = 5
b = 3
print(a * b)   # Output: 15
